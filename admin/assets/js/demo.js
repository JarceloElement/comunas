function getOS() {

var Name_OS = "Unknown OS";
// OS NAME
if (navigator.userAgent.indexOf("Win") != -1) Name_OS = "Windows";
if (navigator.userAgent.indexOf("Mac") != -1) Name_OS = "Macintosh";
if (navigator.userAgent.indexOf("Linux") != -1) Name_OS = "Linux";
if (navigator.userAgent.indexOf("Android") != -1) Name_OS = "Android";
if (navigator.userAgent.indexOf("like Mac") != -1) Name_OS = "iOS";

return Name_OS;
}

function getBrowser() {

// navegador web en escritorio
var sBrowser, sUsrAg = navigator.userAgent;

if(sUsrAg.indexOf("Chrome") > -1) {
    sBrowser = "Chrome";
} else if (sUsrAg.indexOf("Safari") > -1) {
    sBrowser = "Safari";
} else if (sUsrAg.indexOf("Opera") > -1) {
    sBrowser = "Opera";
} else if (sUsrAg.indexOf("Firefox") > -1) {
    sBrowser = "Firefox";
} else if (sUsrAg.indexOf("MSIE") > -1) {
    sBrowser = "Internet Explorer";
}
return sBrowser;

}



function toastjs(message,autohide,time) {
	var targetDiv = document.getElementById('toastjs');
	targetDiv.innerHTML = '<div aria-live="polite" aria-atomic="true" class="d-flex justify-content-center align-items-center" style="height: 50px;"><div id="liveToast" class="toast hide" role="alert" aria-live="assertive" aria-atomic="true" data-delay="2000"><button type="button" class="ml-2 mb-1 close" data-dismiss="toast" aria-label="Close"><span aria-hidden="true">&times;</span></button><div class="toast-body">'+message+'</div></div></div>';

    var myAlert =document.getElementById('liveToast');
    var bsAlert = new bootstrap.Toast(myAlert, {'autohide': autohide, 'delay': time, 'animation': true});
    bsAlert.show();


}


function alertjs(message,autohide,time) {
	var targetDiv = document.getElementById('toastjs');
	targetDiv.innerHTML = '<div class="alert alert-warning" role="alert">'+message+'</div>';
    
    if (autohide == true){
        setTimeout(() => {$('.alert').alert('close')}, 10000);
    }
}


function toastify(message,autohide,time,color) {
    Toastify({
        text: message,
        duration: time,
      //   destination: "https://github.com/apvarun/toastify-js",
      //   newWindow: true,
        close: autohide,
        gravity: "top", // `top` or `bottom`
        position: "center", // `left`, `center` or `right`
        stopOnFocus: true, // Prevents dismissing of toast on hover
        style: {
            background: "linear-gradient(to right,"+alertcolor[color]+")",
            // background: "linear-gradient(to right, #ff8d00, #ffbc00)",
        },
        ariaLive: "polite",
        
        onClick: function(){} // Callback after click
      }).showToast();
}















































